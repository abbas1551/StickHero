package com.example.stilltesing;

public class Platform {
    private int widthDistance;
    private int Position;
    private String PlatformColor;
    private int perfectLandandingRegion;

    public int getWidthDistance() {
        return widthDistance;
    }

    public void setWidthDistance(int widthDistance) {
        this.widthDistance = widthDistance;
    }

    public int getPosition() {
        return Position;
    }

    public void setPosition(int position) {
        Position = position;
    }

    public String getPlatformColor() {
        return PlatformColor;
    }

    public void setPlatformColor(String platformColor) {
        PlatformColor = platformColor;
    }

    public int getPerfectLandandingRegion() {
        return perfectLandandingRegion;
    }

    public void setPerfectLandandingRegion(int perfectLandandingRegion) {
        this.perfectLandandingRegion = perfectLandandingRegion;
    }

}
